SUBROUTINE svg_params(a1,a2,c,ls,s,x1,x2,y1,y2)                              !!!
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
! This subroutine 
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
IMPLICIT NONE                                                                !!!
REAL(KIND=8):: a1(3),a2(3),c(4,3),lx,ly,ls,s,x1,x2,y1,y2           !!!
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
c(1,:)=0*a1+0*a2
c(2,:)=1*a1+0*a2
c(3,:)=0*a1+1*a2
c(4,:)=1*a1+1*a2
lx=(MAXVAL(c(:,1))-MINVAL(c(:,1)))
ly=(MAXVAL(c(:,2))-MINVAL(c(:,2)))
ls=1.2*lx
IF(ly.gt.lx) ls=1.2*ly
s=1.0D3/ls
x1=0.25*SUM(c(:,1))*s-0.5*ls*s
x2=0.25*SUM(c(:,1))*s+0.5*ls*s
y1=0.25*SUM(c(:,2))*s-0.5*ls*s
y2=0.25*SUM(c(:,1))*s+0.5*ls*s
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
END SUBROUTINE svg_params                                                    !!!
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!

!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
SUBROUTINE svg_dist(nv,r,nflags,flag,rflag,drf,dff)    !!!
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
! This subroutine 
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
IMPLICIT NONE                                                                !!!
INTEGER:: i,j,nv,nflags,flag(nflags,-3:3)
REAL(KIND=8):: d,drf,dff           !!!
REAL(KIND=8):: r(nv,3),rflag(nflags,3)                                       !!!
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
drf=1.0D7
DO i=1,nflags
  d=DSQRT(SUM((rflag(i,:)-r(flag(i,3),:))**2))
  IF(d.lt.drf) drf=d 
END DO
dff=1.0D7
DO i=1,nflags
  DO j=1,3
    d=DSQRT(SUM((rflag(i,:)-rflag(flag(i,-j),:))**2))
    IF(d.lt.dff) dff=d 
  END DO  
END DO
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
END SUBROUTINE svg_dist                                                    !!!
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!

!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
SUBROUTINE svg_header(x1,y1,ls,s,f)    !!!
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
! This subroutine 
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
IMPLICIT NONE                                                                !!!
INTEGER:: f
REAL(KIND=8):: x1,y1,ls,s           !!!
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
! SVG header
write(f,*) '<svg viewBox="',x1,y1,ls*s,ls*s,'">'                         !!!
! Defining clip
write(f,*) '<defs>'
write(f,*) '  <clipPath id="cut-square">'
write(f,*) '  <rect x="',x1,'" y="',y1,'" width="',ls*s,'" height="',ls*s,'" />'
write(f,*) '  </clipPath>'
write(f,*) '</defs>'
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
END SUBROUTINE svg_header                                                    !!!
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!

!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
SUBROUTINE make_svg_line(v,u,x1,x2,y1,y2,rad,color,f)                                  !!!
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
! This subroutine 
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
IMPLICIT NONE                                                                !!!
INTEGER:: f
REAL(KIND=8):: x1,y1,x2,y2,rad,v(3),u(3)                                     !!!
CHARACTER(LEN=*):: color
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
IF((v(1).lt.x1).AND.(u(1).lt.x1)) RETURN
IF((v(1).gt.x2).AND.(u(1).gt.x2)) RETURN
IF((v(2).lt.y1).AND.(u(2).lt.y1)) RETURN
IF((v(2).gt.y2).AND.(u(2).gt.y2)) RETURN        
write(f,*) '<line'
write(f,*) 'x1="',v(1),'" y1="',v(2),'"'
write(f,*) 'x2="',u(1),'" y2="',u(2),'"'
write(f,*) 'style="stroke:'//TRIM(ADJUSTL(color))//';stroke-width:',rad,'"'
write(f,*) 'clip-path="url(#cut-square)" />'                                !!!
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
END SUBROUTINE make_svg_line                                                    !!!
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!

!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
SUBROUTINE make_svg_circle(v,x1,x2,y1,y2,rad,rs,color,color_s,f)       !!!
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
! This subroutine 
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
IMPLICIT NONE                                                                !!!
INTEGER:: f
REAL(KIND=8):: x1,y1,x2,y2,rad,rs,v(3)                                     !!!
CHARACTER(LEN=*):: color,color_s
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
IF(v(1).lt.x1) RETURN
IF(v(1).gt.x2) RETURN      
IF(v(2).lt.y1) RETURN
IF(v(2).gt.y2) RETURN              
write(f,*) '<circle'
write(f,*) 'cx="',v(1),'"'
write(f,*) 'cy="',v(2),'"'
write(f,*) 'r="',rad
write(f,*) '" style="stroke:'//TRIM(ADJUSTL(color_s))//';stroke-width:',rs,'"'
write(f,*) 'fill="'//TRIM(ADJUSTL(color))//'"'           !!!
write(f,*) 'clip-path="url(#cut-square)" />'                                !!!
!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!!
END SUBROUTINE make_svg_circle                                                    !!!
